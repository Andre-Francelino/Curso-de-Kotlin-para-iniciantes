// Aula 05 - Operadores Relacionais

fun main(args: Array<String>) {
    /*
        == igual
        < menor que
        > maior que
        <= menor ou igual
        >= maior ou igual
        != diferente de
    */

    var valor1 = 20
    var valor2 = 10

    println(valor1 == valor2)
    println(valor1 < valor2)
    println(valor1 > valor2)
    println(valor1 <= valor2)
    println(valor1 >= valor2)
    println(valor1 != valor2)

}