// Aula 01 - Variáveis mutáveis e imutáveis

fun main(args: Array<String>){
    println("Olá mundo! Bem vindo ao curso de Kotlin para iniciantes.")

    // Variável Mutável - são iniciadas por "var"
    var nome = "André Gomes"
    nome = "André Francelino"
    println(nome)

    // Variável Imutável - são iniciadas por "val"
    val idade = 31
    println(idade)

}
